// for (iniziation; condition; increment)
for (let i = 0; i < 5; i++) {
  console.log("Valor de i: ", i);
}

/* Analicemos la sintaxis del bucle:

Separemos por ; lo que establecemos dentro de los paréntesis for.

Lo primero es la inicialización let i = 0.
Esto ocurre sólo una vez antes de empezar el bucle.

Lo segundo es la condición i < 5.
Esto se comprueba al principio de cada iteración.

Lo tercero es el incremento i++, es decir, i = i + 1.
Esto ocurre al final de cada iteración.
*/
